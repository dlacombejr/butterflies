function [D] = filterplotcolor(W)

Dr=filterplot(W(:,1:size(W,2)/3));
Dg=filterplot(W(:,size(W,2)/3+1:2*size(W,2)/3));
Db=filterplot(W(:,2*size(W,2)/3+1:end));
D=zeros(size(Dr,1),size(Dr,2),3);
D(:,:,1)=Dr;
D(:,:,2)=Db;
D(:,:,3)=Dg;
D = D - min(D(:));
D = D / max(D(:));

end
